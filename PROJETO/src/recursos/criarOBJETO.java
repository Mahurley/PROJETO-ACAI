package recursos;

import telas.item.item;

public class criarOBJETO {
	
//	******************************************************************************************************************************************************************************************************************************************

	public Object[] criarITEMcarrinho(item recebe) {
		Object[] gravador = new Object[4];
		gravador[0] = recebe.getNome();
		gravador[1] = recebe.getValor();
		gravador[2] = 1;
		gravador[3] = recebe.getId();
		return gravador;

	}
//	******************************************************************************************************************************************************************************************************************************************
//	*****************************************************************************************************************************************************************************************************************************************
	

	public Object[] criarITEM(item recebe) {
		Object[] gravador = new Object[4];
		gravador[0] = recebe.getNome();
		gravador[1] = recebe.getValor();
		gravador[2] = recebe.getQuantidade();
		gravador[3] = recebe.getId();
		return gravador;

	}
//	******************************************************************************************************************************************************************************************************************************************


}
